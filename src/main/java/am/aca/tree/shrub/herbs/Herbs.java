package am.aca.tree.shrub.herbs;

public class Herbs{
	private int root;
	private boolean stem;
	private int sheet;
	private boolean flower;
	
	public Herbs(){
		
	}

	public Herbs(int root, boolean stem, int sheet, boolean flower){
		this.root = root;
		this.stem = stem;
		this.sheet = sheet;
		this.flower = flower;
	}
}